package Phase4.array.objectArray;

public class StudentArray {
    private String stdName;
    private Integer age;
    public StudentArray(String stdName, Integer age) {
        this.stdName = stdName;
        this.age = age;
    }
    public String getStdName() {
        return stdName;
    }
    public int getAge(){
        return age;
    }
}
